#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	if (*s1 && *s2)
	{
		while (*s1 && *s2 && *s1 == *s2)
		{
			s1++;
			s2++;
		}
		if (*s1 == *s2)
			return (1);
	}
	if (*s1 == '\0' && *s2 == '\0')
		return (1);
	return (0);
}
