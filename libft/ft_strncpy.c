#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t		i;
	char		*start;

	i = 0;
	start = dst;
	if (dst == NULL && src == NULL)
		return (NULL);
	if (len == 0)
		return (dst);
	while (src[i] != '\0' && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (start);
}
